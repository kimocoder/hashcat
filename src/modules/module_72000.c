typedef struct
{
  // input

  u32 pw_buf[64];
  u32 pw_len;

  // output

  u32 out_buf[64];
  u32 out_len;

} generic_io_tmp_t;

typedef struct
{
  u32 hash_buf[16384];
  u32 hash_len;

  u32 salt_buf[16384];
  u32 salt_len;

} generic_io_t;

